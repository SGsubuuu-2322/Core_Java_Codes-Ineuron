package Default_Package;

public class Loop5 {

	public static void main(String[] args) {
		int n = 5, m = 3;
		for(int  i = 0; i < n; i++){
			System.out.print("* ");
		}
		System.out.println();
		for(int  j = 0; j < m; j++){
			System.out.println("*       *");
		}
		for(int i = 0; i < n; i++){
			System.out.print("* ");
		}
	}

}